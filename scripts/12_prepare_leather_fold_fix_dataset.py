from __future__ import annotations

import argparse
import csv
from pathlib import Path


DEFECT_TYPE = "fold"
SOURCES = ["traditional", "diffusion"]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Prepare the stage12 leather fold recall dataset.")
    parser.add_argument(
        "--stage11-quality-dir",
        type=Path,
        default=Path("outputs") / "stage11_leather_precision_cut_fix" / "quality_filter" / "leather",
    )
    parser.add_argument(
        "--stage12-fold-quality-dir",
        type=Path,
        default=Path("outputs") / "stage12_leather_fold_fix" / "quality_filter" / "fold",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("outputs") / "stage12_leather_fold_fix" / "quality_filter" / "leather",
    )
    return parser.parse_args()


def read_csv(path: Path) -> list[dict[str, str]]:
    if not path.exists():
        raise FileNotFoundError(f"Required CSV not found: {path.as_posix()}")
    with path.open("r", encoding="utf-8", newline="") as file:
        return list(csv.DictReader(file))


def write_csv(rows: list[dict[str, object]], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if not rows:
        path.write_text("", encoding="utf-8")
        return
    fieldnames: list[str] = []
    for row in rows:
        for key in row:
            if key not in fieldnames:
                fieldnames.append(key)
    with path.open("w", encoding="utf-8", newline="") as file:
        writer = csv.DictWriter(file, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def prepare_traditional_rows(stage11_dir: Path, stage12_dir: Path) -> list[dict[str, str]]:
    old_rows = read_csv(stage11_dir / "accepted_traditional_summary.csv")
    new_rows = read_csv(stage12_dir / "accepted_traditional_summary.csv")
    new_fold = [row for row in new_rows if row["defect_type"] == DEFECT_TYPE]
    if not new_fold:
        raise ValueError("No accepted stage12 traditional fold rows found.")
    return old_rows + new_fold


def prepare_diffusion_rows(stage11_dir: Path) -> list[dict[str, str]]:
    return read_csv(stage11_dir / "accepted_diffusion_summary.csv")


def summarize(rows: list[dict[str, str]]) -> list[dict[str, object]]:
    summary_rows: list[dict[str, object]] = []
    defect_types = sorted({row["defect_type"] for row in rows})
    for source in SOURCES:
        for defect_type in defect_types:
            subset = [
                row for row in rows
                if row.get("synthetic_source", source) == source and row["defect_type"] == defect_type
            ]
            if not subset:
                continue
            summary_rows.append({
                "synthetic_source": source,
                "defect_type": defect_type,
                "accepted": len(subset),
            })
    return summary_rows


def write_summary_md(summary_rows: list[dict[str, object]], output_path: Path) -> None:
    lines = [
        "# Stage 12 Leather Fold Fix Dataset",
        "",
        "This file is generated by scripts/12_prepare_leather_fold_fix_dataset.py.",
        "",
        "Strategy: keep the stable stage11 accepted leather set and append accepted stage12 traditional fold samples.",
        "Stage12 diffusion fold samples are analyzed but not added to training because they increase background change.",
        "",
        "| source | defect_type | accepted |",
        "| --- | --- | ---: |",
    ]
    for row in summary_rows:
        lines.append(f"| {row['synthetic_source']} | {row['defect_type']} | {row['accepted']} |")
    output_path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    args = parse_args()
    traditional = prepare_traditional_rows(args.stage11_quality_dir, args.stage12_fold_quality_dir)
    diffusion = prepare_diffusion_rows(args.stage11_quality_dir)
    accepted = traditional + diffusion
    summary_rows = summarize(accepted)

    args.output_dir.mkdir(parents=True, exist_ok=True)
    write_csv(traditional, args.output_dir / "accepted_traditional_summary.csv")
    write_csv(diffusion, args.output_dir / "accepted_diffusion_summary.csv")
    write_csv(accepted, args.output_dir / "accepted_summary.csv")
    write_csv(summary_rows, args.output_dir / "filter_summary.csv")
    write_summary_md(summary_rows, args.output_dir / "summary.md")

    print("Stage 12 leather fold fix dataset prepared.")
    print(f"Output dir: {args.output_dir.as_posix()}")
    print(f"Traditional accepted: {len(traditional)}")
    print(f"Diffusion accepted: {len(diffusion)}")
    print(f"Total accepted: {len(accepted)}")


if __name__ == "__main__":
    main()
